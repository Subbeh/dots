hl.on("hyprland.start", function()
  hl.exec_cmd("__hypr_tmux_sessions")
  hl.exec_cmd("systemctl --user enable --now hyprpolkitagent.service")
  hl.exec_cmd("wl-paste --type text --watch cliphist store")
  hl.exec_cmd("wl-paste --type image --watch cliphist store")
  hl.exec_cmd("uwsm app -s a -- swaync")
  hl.exec_cmd("uwsm app -s b -- hypridle")
  hl.exec_cmd("uwsm app -s b -- swaybg -o \\* -i ~/.local/share/wallpapers/earth.jpg -m fill")
  hl.exec_cmd("uwsm app -s b -- Thunar --daemon")
  hl.exec_cmd("uwsm app -- kitty.desktop", { workspace = "special:term", no_initial_focus = true })
  StartWaybar(Monitors.laptop.name)
end)

hl.on("monitor.added", function()
  ReloadWaybar()
end)

hl.on("monitor.removed", function()
  ReloadWaybar()
end)
